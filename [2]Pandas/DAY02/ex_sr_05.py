# ======================================================================
#                   Series 인스턴스 원소/요소 다루기
# ----------------------------------------------------------------------
# * 읽기 : 인스턴스변수명[인덱스]
# * 변경 : 인스턴스변수명[인덱스] = 새로운 값
# ======================================================================
# ----------------------------------------------------------------------
# [1] 모듈 로딩
# ----------------------------------------------------------------------
import pandas as pd
import func as f

# ----------------------------------------------------------------------
# [2] Series 인스턴스 생성
# ----------------------------------------------------------------------
# 데이터 준비
data1 = [22, 33, 11, 12, 25]

# Series 인스턴스 생성
sr1 = pd.Series(data1)
sr2 = pd.Series(data1, index=['one', 'two', 'three', 'four', 'five'])

# ----------------------------------------------------------------------
# [3] 원소값 읽기 : 변수명[인덱스]
#                   ★ 음수 인덱스 미지원
# ----------------------------------------------------------------------
print("인덱스", sr1.index)
print(f'sr1[0] : {sr1[0]},  sr1[4] : {sr1[4]}')
# print(f'sr1[0] : {sr1[0]}, sr1[-1] : {sr1[-1]}')      # ERROR 미지원

print(f"[여러개 요소 읽기] sr1[[0, 2]] => \n{sr1[[0, 2]]}")
print(f"[여러개 요소 읽기] sr1[[0, 1, 2]] => \n{sr1[[0, 1, 2]]}")
print(f"[여러개 요소 읽기] sr1[[0]] => \n{sr1[[0]]}")

# ----------------------------------------------------------------------
# RangeIndex 외에 설정한 인덱스가 존재하는 경우
# 인덱스라벨과 위치인덱스가 존재하게 됨
# => 원소 접근 방법 2개 존재 : 라벨, 위치 인덱스
# ----------------------------------------------------------------------
print("인덱스", sr2.index)
print(f'[인덱스라벨] sr2["one"] : {sr2["one"]},  sr1["five"] : {sr2["five"]}')
print(f"[위치인덱스] sr2[0] : {sr2[0]}, sr2[4] : {sr2[4]}")
print(f"[위치인덱스] sr2.iloc[0] : {sr2.iloc[0]}, sr2.iloc[4] : {sr2.iloc[4]}")
print(sr2)