# (1) 사용할 기능 포함한 모듈 로딩
import math as m

# 모듈에 포함된 기능 사용하기
# -> 형식 : 모듈명.변수명 / 모듈명.함수명() / 모듈명.클래스명()
print(f'math.pi  : {m.pi}')
print(f'math.inf : {m.inf}')
print(f'math.factorial(5) : {m.factorial(5)}')
print(f'math.pow(2, 15) : {m.pow(2, 15)}')