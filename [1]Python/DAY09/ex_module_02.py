# =======================================================
# 모듈 : 특정 목적의 함수, 변수, 클래스로 구성된 py파일
# -> 종류
#   * 표준      모듈 : python에서 제공
#   * 사용자정의 모듈 : 개인/개발자 만드는 것
#   * 써드파티션 모듈 : 기업/연구소/개인 만드는 것 => 설치 필수
