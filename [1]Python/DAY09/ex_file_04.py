# ===================================================================
# 파일 입출력 - 파일 쓰기
# ===================================================================
# -------------------------------------------------------------------
# 동작 프로세스
# (1) 파일 열기 --- 쓰기 모드, 인코딩 방식 설정 체크
# (2) 내용 쓰기
# (3) 파일 닫기
# -------------------------------------------------------------------
# 전역 변수 
# -------------------------------------------------------------------
file_path = './mydata.txt'

# -------------------------------------------------------------------
# 파일 쓰기 - 2 : a 모드
#               append 약자
#               존재하지 않는 파일이면 생성
#               존재하는 파일이면 내용 끝부분에 내용추가
# -------------------------------------------------------------------
# (1) 열기 => open()
f = open(file_path, mode='at', encoding='utf-8')

# (2) 쓰기 
wCnt = f.write("왕사남\n")
print(f"쓰기한 횟수 : {wCnt}개")

wCnt = f.write("크게는 재미없었엉")
print(f"쓰기한 횟수 : {wCnt}개")

# (3) 닫기
f.close()

